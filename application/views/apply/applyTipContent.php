一、英国留学PS(personal statement)的主旨</p>
<p>
很多英国大学无需面试申请人，所以他们唯一认识你的地方就是UCAS form(英国大学网上申请表格)。大部分表格涵盖了你的个人信息，包括大学感兴趣的你的学术/语言成绩、推荐信以及PS(留学个人陈述)。

PS(留学个人陈述)是这些材料当中唯一一个由你来控制的，所以这个是你给大学招生办留下一个良好印象的机会，这个印象是用分数无法反映出来的。尤其是当你申请的学校竞争特别激烈，所有的申请者都有很好的成绩的时候，PS就是唯一一种能凸显你的与众不同的材料，所以同志们，一定要尽可能地把PS做到尽善尽美。
</p><p>
大学招生办和课程老师在看你的PS的时候，他们通常会问以下两个问题：</p>

1、我们是否想要这个学生来学习这门课程?

2、我们是否想要这个学生来我们的学校学习?

这两个问题可以分解为以下几个小问题来解答：

1)这个学生是否适合他/她所申请的课程?

2)这个学生是否具备他/她所申请的课程需要的素质?

3)这个学生是否有足够的毅力、勤奋的品格以及不会轻易辍学?

4)这个学生是否会全力以赴地达到这门课程所要求的各项能力?

5)这个学生是否能在高压底下工作?

6)这个学生是否能很快得调整好自己融入到大学的新环境?

7)他们的交际能力如何?

8)他们是否热爱这个课程并且做了仔细的研究

9)他们对课程是否有真正的兴趣并且渴望去做更深入的学习?
<p>
这些问题就是需要你在PS当中去表述和解答，只不过你不能只是说YES和NO，你需要有足够的事实依据去证明你的说法，让你的观点言之有物。不过，招生办的老师们毕竟也是平凡人，他们有可能需要阅读成百上千的PS，所以即便你觉得已经对上述问题回答的很好了你还有可能被不幸地拒绝。

还有其他的一些技巧可以让你的PS脱颖而出给老师们留下深刻印象，但要记住老师们也都因人而异他们对于申请者的期望也都不完全相同，怎么把握当中玄机就看童鞋们自己拿捏了。

这个教程会进一步探讨这当中可以借鉴的技巧。</p>
      
